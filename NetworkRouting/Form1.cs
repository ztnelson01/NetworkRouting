        private PriorityQueue Dijkstra(String type)
        {
            double maxTime = 0;
            String whichType = "";
            Stopwatch myWatch = new Stopwatch();
         

            PriorityQueue pq = null;
            if (type == "array")
            {
                pq = new ArrayQueue(points.Count);
            }
            else if (type == "heap")
            {
                pq = new HeapQueue(points.Count);
            }
            pq.changeDistance(startNodeIndex, 0);
            pq.formerIndexes[startNodeIndex] = startNodeIndex;
            for (int i = 0; i < points.Count - 1; i++)
            {
                myWatch.Reset();
                myWatch.Start();
                int deletedIndex = pq.deleteMinimum();
                myWatch.Stop();
                //Console.WriteLine("deleteTime: " + myWatch.Elapsed.TotalSeconds.ToString());
                if (myWatch.Elapsed.TotalSeconds > maxTime) { maxTime = myWatch.Elapsed.TotalSeconds; whichType = "deleteTime"; }
                foreach (int neighbor in adjacencyList[deletedIndex])
                {
                    double newDistance = distanceAtoB(points[deletedIndex], points[neighbor]) + pq.distances[deletedIndex];
                    if (newDistance < pq.distances[neighbor])
                    {
                        myWatch.Reset();
                        myWatch.Start();
                        pq.changeDistance(neighbor, newDistance);
                        myWatch.Stop();
                        //Console.WriteLine("changeDistanceTime: " + myWatch.Elapsed.TotalSeconds.ToString());
                        if (myWatch.Elapsed.TotalSeconds > maxTime) { maxTime = myWatch.Elapsed.TotalSeconds; whichType = "changeDistanceTime"; }
                        pq.formerIndexes[neighbor] = deletedIndex;
                    }
                }
            }
            //Console.WriteLine("MaxTime: " + whichType + " " + maxTime);
            return pq;
        }

        private void solveButton_Clicked()
        {
            Stopwatch myWatch = new Stopwatch();
            myWatch.Start();
            PriorityQueue pq = Dijkstra("heap");
            myWatch.Stop();
            arrayTimeBox.Text = myWatch.Elapsed.TotalSeconds.ToString();
            int current = stopNodeIndex;
            Pen myPen = new Pen(Color.Red);
            SolidBrush drawBrush = new SolidBrush(Color.Black);
            Font myFont = new Font("Arial", 16);
            while (true)
            {
                PointF point1 = points[current];
                PointF point2 = points[pq.formerIndexes[current]];
                PointF midPoint = new PointF((point1.X + point2.X) / 2, (point1.Y + point2.Y) / 2);
                graphics.DrawLine(myPen, point1, point2);
                graphics.DrawString(((int)distanceAtoB(point1, point2)).ToString(), myFont, drawBrush, midPoint);
                current = pq.formerIndexes[current];
                if (pq.formerIndexes[current] == current) break;
            }
            pathCostBox.Text = pq.distances[stopNodeIndex].ToString();
            if (pq.distances[stopNodeIndex] == Double.MaxValue) pathCostBox.Text = "No Path";
            
        }

